export class Virtus {
    sensibilitas: number;
    ingenium: number;
    ratio: number;
    auctoritas: number;
    vigor: number;
    coordinatio: number;

    constructor(sensibilitas: number, ingenium: number, ratio: number, auctoritas: number, vigor: number, coordinatio: number) {
        this.sensibilitas = sensibilitas;
        this.ingenium = ingenium;
        this.ratio = ratio;
        this.auctoritas = auctoritas;
        this.vigor = vigor;
        this.coordinatio = coordinatio;
    }
}
